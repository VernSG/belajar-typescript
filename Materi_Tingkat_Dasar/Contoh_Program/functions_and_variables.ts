// Deklarasi variabel
let message: string = "Hello, TypeScript!";

// Fungsi sederhana untuk menyapa
function greet(name: string): string {
    return `Hello, ${name}!`;
}

// Memanggil fungsi greet
let greeting: string = greet("Alice");
console.log(greeting);

// Menampilkan pesan dari variabel
console.log(message);
